package Snippets;

public class Mod
{
    public static void main(String args[])
    {
        // mod обозначается знаком %, возвращает остаток от деления
        int i = 17; // Целые
        double d = 17.3; // С плавающей точкой

        System.out.println("i = " + i + ", i mod 10 = " + i % 10);
        System.out.println("d = " + d + ", d mod 10 = " + d % 10);
    }
}
